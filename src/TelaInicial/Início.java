public void ExcluirExtintor() throws SQLException{//Método para Excluir o registro

            int id_ordem;    
         id_ordem = Integer.parseInt(txtOrdem.getText());
         
         LigacaoDTO objLigacaoDTO = new LigacaoDTO();
         objLigacaoDTO.setId_Ordem(id_ordem);
                  
         LigacaoDAO objLigacaoDAO = new LigacaoDAO();
         objLigacaoDAO.Excluir(objLigacaoDTO);

    }


        private void ConsultarExtintor() {//Método para atualizar a lista após alteraçoes

        try {
                        
            LigacaoDAO objLigacaoDAO = new LigacaoDAO();
            
            DefaultTableModel model = (DefaultTableModel) tbl_extin.getModel(); //Parte criada para inserir uma table
            model.setNumRows(0);//método pra inserir novas linhas após a inserção dos valores
           
            ArrayList<LigacaoDTO> lista = objLigacaoDAO.ConsultarDAO(); //criação do array pra incrementar os dados na table
                    
            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getTipo(), //o get para pegar os dados
                    lista.get(num).getNumero(),
                    lista.get(num).getValidade(),
                    lista.get(num).getLocalizaçao(),
                    lista.get(num).getId_Ordem(),
                        
                        
                                                       
                });      
            }               

         } catch (SQLException erro) {

            JOptionPane.showMessageDialog(null, "consultar extintor" + erro);
                 }
        
        } 
 

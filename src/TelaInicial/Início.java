private void Cadastrar() throws SQLException {//Método para Cadastrar o registro
    
        String tipo; //declaraçao das variáveis
        String Localização;
        String validade;
        Integer id_ordem, numero;
        
        tipo = (String) txttipos.getSelectedItem();//pegando os dados do combobox com o getselectedItem
        Localização = txtLocalizaçao.getText();
        id_ordem = Integer.parseInt(txtOrdem.getText()); //dados integer sendo convertidos para String
        validade = txtValidade.getText();
        numero = Integer.parseInt(txtnumero.getText());
        
        LigacaoDTO ObjLigacaoDTO = new LigacaoDTO();
        
        ObjLigacaoDTO.setNumero(numero);
        ObjLigacaoDTO.setLocalizaçao(Localização);
        ObjLigacaoDTO.setId_Ordem(id_ordem);
        ObjLigacaoDTO.setValidade(validade);
        ObjLigacaoDTO.setTipo(tipo);
        
        LigacaoDAO  ObjLigacaoDAO = new LigacaoDAO();
        ObjLigacaoDAO.CadastrarDAO(ObjLigacaoDTO);
               
    }

